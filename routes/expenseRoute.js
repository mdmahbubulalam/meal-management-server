const { getAllExpense, addExpense, singleExpense, updateExpense, deleteExpense } = require('../controllers/expenseController');

const expenseRoute = require('express').Router();

expenseRoute.get('/allExpense', getAllExpense)
expenseRoute.post('/addExpense', addExpense)
expenseRoute.get('/:expenseId', singleExpense)
expenseRoute.put('/:expenseId', updateExpense)
expenseRoute.delete('/:expenseId', deleteExpense)
module.exports = expenseRoute;